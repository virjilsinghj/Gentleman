            <div>
                <h1 class="vj-center vj-wide vj-text-grey ">SEARCH ENGINE OPTIMIZATION</h1>
            </div>
            <p>Here we’ll take a look at the basic things you need to know in regards to search engine optimisation, a
                discipline that everyone in your organisation should at least be aware of, if not have a decent
                technical understanding.
            </p>
            <p>
                One of our most popular articles of all time is a post entitled SEO Basics: 8 Essentials When Optimizing
                Your Site. It still does the business for us in terms of traffic, however it was first published in
                April 2013, so you can treat this as its long overdue and expanded update.
            </p>
            <strong class="vj-white">
                <h2>What is SEO?</h2>
            </strong>
            <p>

                Quite simply, SEO is the umbrella term for all the methods you can use to ensure the visibility of your
                website and its content on search engine results pages (SERPs).
            </p>
            <p>
                The methods vary from technical practices you can achieve behind the scenes on your website (we tend to
                refer to this as ‘on-page SEO’) to all the promotional ’off-page’ approaches you can use to raise your
                site’s visibility (link-building, social media marketing).
            </p>
            <p>
                For the purpose of this article, when we talk about visibility, we mean how high up the SERP your
                website appears for certain search terms in the ‘organic’ results. Organic results refer to those that
                appear naturally on the page, rather than in the paid-for sections…
            </p>
            <p>
                search for confetti cannon showing organic and paid results
            </p>
            <p>
                Paid search is also a large part of search engine marketing. You can read more about this in our recent
                beginner’s guide to paid search and PPC.
            </p>
            <strong class="vj-white">
                <h2>Why do you need SEO?</h2>
            </strong>
            <p>

                Building a strong site architecture and providing clear navigation will help search engines index your
                site quickly and easily. This will also, more importantly, provide visitors with a good experience of
                using your site and encourage repeat visits. It’s worth considering that Google is increasingly paying
                attention to user experience.
            </p>
            <p>
                When it comes to how much traffic is driven by search engines to your website, the percentage is
                substantial, and perhaps the clearest indicator of the importance of SEO.
            </p>
            <p>
                In 2014, Conductor suggested 64% of all web traffic comes from organic search, compared to 2% from
                social, 6% from paid search, 12% direct and 15% from other referral sources.
            </p>
            <p>
                This tallies with our own data, with approximately 70-75% of SEW traffic coming from organic.
            </p>
            <p>
                Of all organic traffic, in 2015 it was found that Google accounts for more than 90% of global organic
                search traffic. So obviously you need a strong presence on Google SERPs, but how strong?
            </p>
            <p>
                Well, according to this study from Advanced Web Ranking (which I’ve trotted out before when discussing
                how to dominate Google) shows that on the first SERP, the top five results account for 67.60% of all
                clicks and the results from six to 10 account for only 3.73%.
            </p>
            <p>

                It’s therefore vital that your site appears in the top five results.
            </p>
            <p>
                How are you going to achieve this? With the following tips, which I’ve split into two categories: what
                search engines are looking for and… drum roll… what they’re not looking for.
            </p>
            <strong class="vj-white">
                <h2> What are search engines looking for?</h2>
            </strong><br>
            <strong class="vj-white">
                <h2>1) Relevancy</h2>
            </strong>
            <p>
                Search engines try to provide the most relevant results to a searcher’s query, whether it’s a simple
                answer to the question “how old is Ryan Gosling?” (the answer of which Google will likely provide
                without you having to leave the SERP) to more complicated queries such as “what is the best steak
                restaurant nearest to me?”
            </p>
            <p>
                where's good for steak search

                How search engines provide these results is down to their own internal algorithms, which we’ll probably
                never truly determine, but there are factors that you can be certain will influence these results and
                they’re all based around relevancy… For instance: a searcher’s location, their search history, time of
                day/year, etc.
            </p>
            <strong class="vj-white">
                <h2>2) The quality of your content</h2>
            </strong>
            <p>

                Do you regularly publish helpful, useful articles, videos or other types of media that are popular and
                well produced? Do you write for actual human beings rather than the search engine itself? Well, you
                should. Latest research from Searchmetrics on ranking factors indicates that Google is moving further
                towards longer-form content that understands a visitor’s intention as a whole, instead of using keywords
                based on popular search queries to create content.

                Basically, stop worrying about keywords and focus on the user experience.
            </p>
            <strong class="vj-white">
                <h2>3) User experience</h2>
            </strong>
            <p>

                There are many SEO benefits for providing the best possible user experience. You need an easily
                navigable, clearly searchable site with relevant internal linking and related content. All the stuff
                that keeps visitors on your webpage and hungry to explore further.
            </p>
            <strong class="vj-white">
                <h2>4) Site speed</h2>
            </strong>
            <p>

                How quickly your webpages load is increasingly becoming a differentiator for search engines. Google may
                soon start labelling results that are hosted on Accelerated Mobile Page (AMP) so this may possibly be
                the ‘mobilegeddon’ of 2016. Speaking of which…
            </p>
            <strong class="vj-white">
                <h2>5) Cross-device compatibility</h2>
            </strong>
            <p>

                Is your website and its content equally optimised for any given screen size or device? Bear in mind that
                Google has stated that responsive design is its preferred method of mobile optimisation.
            </p>
            <strong class="vj-white">
                <h2>6) Internal linking</h2>
            </strong>
            <p>

                We’ve talked about the benefits of ensuring your site has clear and easy-to-use navigation, but there’s
                also a practice that editors and writers can carry out when publishing articles to help push traffic
                around the site and that may lead to higher trust signals for Google: internal linking. (See what we did
                there.)
            </p>
            <p>
                Internal linking has many advantages:
            </p>
            <strong>It provides your audience with further reading options.</strong> As long as they’re relevant and you
            use clear anchor text (the clickable highlighted words in any give link). This can help reduce your bounce
            rates.<br>
            <strong>It helps to improve your ranking for certain keywords.</strong> If we want this article to rank for
            the term ’SEO basics’ then we can begin linking to it from other posts using variations of similar anchor
            text. This tells Google that this post is relevant to people searching for ‘SEO basics’. Some experts
            recommend varying your anchor text pointing to the same page as Google may see multiple identical uses as
            ‘suspicious’.<br>
            <strong>It helps Google crawl and index your site.</strong> Those little Googlebots that are sent out to
            fetch new information on your site will have a better idea of how useful and trustworthy your content is,
            the more they crawl your internal links.<br>
            </p>
            <strong class="vj-white">
                <h2>7) Authority</h2>
            </strong>
            <p>

                An authority website is a site that is trusted by its users, the industry it operates in, other websites
                and search engines. Traditionally a link from an authority website is very valuable, as it’s seen as a
                vote of confidence. The more of these you have, and the higher quality content you produce, the more
                likely your own site will become an authority too.
            </p>
            <p>
                However as the aforementioned Searchmetrics research suggests, year-on-year correlations between
                backlinks and rankings are decreasing, so perhaps over time ‘links’ may not be as important to SEO as we
                once thought.
            </p>
            <p>
                There’s a good argument raging in the comments to this recent piece on links as a marketing KPI, which
                offers some diverse views on the subject.
            </p>
            <strong class="vj-white">
                <h2>8) Meta descriptions and title tags</h2>
            </strong>
            <p>

                Having a meta description won’t necessarily improve your ranking on the SERP, but it is something you
                should definitely use before publishing an article as it can help increase your chances of a searcher
                clicking on your result.
            </p>
            <p>
                The meta description is the short paragraph of text that appears under your page’s URL in the search
                results, it’s also something you should have complete control of in your CMS.
            </p>
            <p>

                <strong>Title tags</strong> are used to tell search engines and visitors what your site is about in the
                most concise and accurate way possible. The keywords in your title tag show up highlighted in search
                engine results (if the query uses those keywords), as well as in your browser tab and when sharing your
                site externally.
            </p>
            <p>
                You can write your own title tag inside the &lt;head&gt; area of your site’s HTML:
            </p>
            <p><strong>
                    &lt;head&gt;<br>
                    &lt;title&gt;Example Title &lt;/title&gt;<br>
                    &lt;/head&gt;
                </strong></p>
            <p>
                You should use a few accurate keywords describing the page as well as your own brand name. Only use
                relevant keywords though, and the most important thing to consider is that although you are formatting
                for search engines, you should write for humans.
            </p>
            <p>
                There is a lot more practical guidance to be found in our complete guide to title tags.
            </p>
            <strong class="vj-white">
                <h2>9) Schema markup</h2>
            </strong>
            <p>

                You can make your search results appear more attractive by adding Schema markup to the HTML of your
                pages. This can help turn your search results into a rich media playground, adding star-ratings,
                customer ratings, images, and various other bits of helpful info…
            </p>
            <p>
                Schema is also the preferred method of markup by most search engines including Google, and it’s fairly
                straightforward to use. For more information, check out our handy guide to Schema.
            </p>
            <strong class="vj-white">
                <h2>10) Properly tagged images</h2>
            </strong>
            <p>

                Many people forget to include the alt attribute when they upload images to their content, but this is
                definitely something you shouldn’t overlook because Google cannot ‘see’ your images, but can ‘read’ the
                alt text.
            </p>
            <p>
                By describing your image in the alt text as accurately as possible it will increase the chances of your
                images appearing in Google Image search.
            </p>
            <p>

                It will also improve the accessibility of your site for people using ‘screen reader’ software.
            </p>
            <strong class="vj-white">
                <h2>11) Evergreen content</h2>
            </strong>
            <p>

                Instead of peppering the internet with a rash of ‘quick win’ news stories with little insight, why not
                publish more evergreen content.
            </p>
            <p>
                More thoughtful, helpful and practical-advice based articles can lead to huge long-term wins in terms of
                driving traffic and occupying highly visible positions in the SERPs.
            </p>
            <p>
                Here’s a guide to planning and creating evergreen content.
            </p>
            <strong class="vj-white">
                <h2>12) Domain names</h2>
            </strong>
            <p>

                You should use <strong>sub-directory root domains</strong> (searchenginewatch.com/category/seo) instead
                of sub-domains (searchenginewatch.category.seo.com) as this is better for your overall site
                architecture.
            </p>
            <p>
                You should also stay away from hyphens (search-engine-watch.com) and alternative Top-level domain names
                (.biz .name .info) as these are considered spammy.
            </p>
            <p>
                Having a ‘keyword rich’ domain name may lead to closer scrutiny from Google. According to Moz, Google
                has “de-prioritized sites with keyword-rich domains that aren’t otherwise high-quality. Having a keyword
                in your domain can still be beneficial, but it can also lead to closer scrutiny and a possible negative
                ranking effect from search engines—so tread carefully.”
            </p>
            <p>
                Also you should make sure that if you operate a site without the www. prefix, someone who types in
                www.example.com will still be redirected to your site. If this isn’t happening, Google may assume these
                are two different sites and your visibility could be compromised.
            </p>
            <strong class="vj-white">
                <h2>13) Headlines and permalinks</h2>
            </strong>
            <p>

                The headlines for your articles should be <strong>under 55 characters</strong> to ensure their complete
                visibility in SERPs. Make sure they’re snappy, attractive and as descriptive as possible (this is often
                an impossible balance). Just stay away from clickbait headlines, do not promise something that the
                content doesn’t deliver.
            </p>
            <p>
                The permalink (or URL), which you can normally alter in your CMS even after it’s been set automatically,
                doesn’t necessarily have to match the headline exactly. Google has stated that you can use three to four
                key words that you should put the most important keywords first.
            </p>
            <strong class="vj-white">
                <h2>14) Comments</h2>
            </strong>
            <p>

                Do not turn off your comments system. Having a thriving community of regular commenters engaging in
                dialogue under your posts shows that visitors care enough about your content to either make their own
                relevant points or to praise it or to ruthlessly eviscerate it. Either way, at least people are reading
                it.
            </p>
            <p>
                Just be super-mindful about filtering out spam comments, or immediately removing any that slip through.
                It’s also worth adding the nofollow value to your comments section so Google ignores any erroneous links
                that may appear.
            </p>
            <strong class="vj-white">
                <h2>15) Local SEO</h2>
            </strong>
            <p>

                Increasingly Google is serving results to users based on their location. This is particularly important
                to businesses out there in the real world who ned to catch a searcher’s attention just at the right
                moment, i.e. while walking down the street, on their mobile and looking for somewhere to eat.
            </p>
            <p>
                You should register with Google My Business and ensure that all of your information is accurate and
                up-to-date, such as opening times, contact information, customer reviews and that your categorised
                correctly.
            </p>
            <strong class="vj-white">
                <h2>16) Social</h2>
            </strong>
            <p>

                The most obvious way that you can raise your site’s visibility through non-technical SEO means is of
                course through social media marketing.
            </p>
            <p>
                You need to make sure you’re present on all relevant social channels (wherever your audience may be),
                and not just broadcasting your content in a faceless manner, but by using it as a customer service
                channel and genuinely interacting with people in a friendly, helpful and entertaining manner.
            </p>
            <p>
                The actual correlation between social signals and search rankings is a much argued over subject, but
                here’s a good overview of the subject.
            </p>
            <p>
                <strong>What are search engines NOT looking for?</strong>
            </p>
            <p>
                There are many ‘black hat’ practices that can bring the full weight of a Google penalty down on your
                site, so it’s best to avoid doing the following, even if it looks like a brilliant easy win at the time.
            </p>
            <strong class="vj-white">
                <h2>17) Keyword stuffing</h2>
            </strong>
            <p>

                Overusing keywords on your pages, especially when they obviously affect the readability of your site.
                It’s debatable whether Google even still uses keywords as a ranking factor anymore.
            </p>
            <strong class="vj-white">
                <h2>18) Link buying or excessive link exchanging</h2>
            </strong>
            <p>

                Thinking of approaching a link farm? Just don’t do it. It’s not even worth it. The most valuable links
                to your site are the ones that come from authority sites within your own niche.
            </p>
            <strong class="vj-white">
                <h2>19) Annoying ads</h2>
            </strong>
            <p>

                Anything overly intrusive that destroys the pleasure of reading your content and slows down your site
                speed.
            </p>
            <strong class="vj-white">
                <h2>20) Mobile app interstitials</h2>
            </strong>
            <p>

                If you present mobile visitors with a full-screen advert to download your app, Google will consider you
                no longer mobile friendly.


            </p>
            <strong class="vj-white">
                <h2>21) Duplicated content</h2>
            </strong>
            <p>

                If Google finds two identical pieces of content, whether on your own site, or on another you’re not even
                aware of, it will only index one of those pages. You should be aware of scraper sites, stealing your
                content automatically and republishing as your own. Here’s Graham Charlton’s thorough investigation on
                what to if your content ends up working better for somebody else.
            </p>
            <strong class="vj-white">
                <h2>22) Hidden text and links</h2>
            </strong>
            <p>

                There are a ways to manipulate rankings that a user may not ever see, but Google will probably find and
                punish you for.
            </p>
            <p>
                Stay away from using white text on a white background, positioning text off-screen, setting font size to
                zero or hiding a link in a single character like a comma or a full-stop.
            </p>
            <p>
                The temptation to put a link in that last full stop was incredibly high.
            </p>
            <p>
                Right, on that note, I’ll finish up. Clearly this isn’t everything that you can do to help your initial
                SEO efforts, but it’s a good grounding at least
            </p>
            <p>
                Please let me know if I’ve missed anything vital or just got something plain wrong…
            </p>
